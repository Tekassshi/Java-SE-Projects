package data;

public class PersonParamsContainer {

    private Long id;

    private String name;
    private String xCoord;
    private String yCoord;

    private String height;
    private String weight;
    private String eyeColor;
    private String nationality;
    private String xLooc;
    private String yLooc;
    private String zLooc;

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getxCoord() {
        return xCoord;
    }

    public void setxCoord(String xCoord) {
        this.xCoord = xCoord;
    }

    public String getyCoord() {
        return yCoord;
    }

    public void setyCoord(String yCoord) {
        this.yCoord = yCoord;
    }

    public String getHeight() {
        return height;
    }

    public void setHeight(String height) {
        this.height = height;
    }

    public String getWeight() {
        return weight;
    }

    public void setWeight(String weight) {
        this.weight = weight;
    }

    public String getEyeColor() {
        return eyeColor;
    }

    public void setEyeColor(String eyeColor) {
        this.eyeColor = eyeColor;
    }

    public String getNationality() {
        return nationality;
    }

    public void setNationality(String nationality) {
        this.nationality = nationality;
    }

    public String getxLooc() {
        return xLooc;
    }

    public void setxLooc(String xLooc) {
        this.xLooc = xLooc;
    }

    public String getyLooc() {
        return yLooc;
    }

    public void setyLooc(String yLooc) {
        this.yLooc = yLooc;
    }

    public String getzLooc() {
        return zLooc;
    }

    public void setzLooc(String zLooc) {
        this.zLooc = zLooc;
    }
}
